    private void ConfirmarVoto() {
        Connection con = Conexao.abrirConexao();
        CandidatoDAO sql = new CandidatoDAO(con);
        EleicaoDAO adao = new EleicaoDAO(con);
        int cod = adao.verificarEleicao();
        System.out.println("Codigo da eleição:(ConfirmarVoto) "+cod);
            if(cod!=0){
                int codCandidato = Integer.parseInt(jtfCod.getText());
                System.out.println("Código candidato(JIVotar):"+codCandidato);
                boolean v = sql.TestarCandidato(codCandidato);
                System.out.println("Boolean testarCandidato(JinternalVotar):"+v);
                if (v==true) {
                    votarCandidato();
                }else{
                    Object[] options = { "Sim", "Não" };
                    int opcao = JOptionPane.showOptionDialog(null, "Candidato não encontrado, deseja votar nulo?", "JKLR Sistemas", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, options,options[0]);
                        if (opcao==0) {
                            votarNulo();
                        } else {
                            CorrigirVoto();
                        }
                }
            }else{
                this.dispose();
                JOptionPane.showMessageDialog(null, "Nenhuma eleição encontrada, encerre a atual e tente novamente.","JKLR Sistemas",JOptionPane.PLAIN_MESSAGE);
            }
                
    }

