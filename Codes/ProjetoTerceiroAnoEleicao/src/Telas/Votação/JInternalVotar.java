    private void jbVotarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbVotarActionPerformed
        String c = jtfCod.getText();
        if(c.equalsIgnoreCase("")){
            Object[] options = { "Sim", "Não" };
                    int opcao = JOptionPane.showOptionDialog(null, "Nenhum número encontrado, deseja votar nulo?", "JKLR Sistemas", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, options,options[0]);
                        if (opcao==0) {
                            votarNulo();
                            bVotar=true;
                            verificarSelecionada();
                        } else {
                            CorrigirVoto();
                            b1=true;
                            verificarSelecionada();
                        }
        }else{
           ConfirmarVoto();
           bVotar=true;
           verificarSelecionada();
        }   
    }//GEN-LAST:event_jbVotarActionPerformed

