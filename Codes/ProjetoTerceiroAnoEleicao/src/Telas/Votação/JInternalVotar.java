    private void confirmar() {
        String cod = jtfSenha.getText();
        Connection con = Conexao.abrirConexao();
        AdminDAO sql = new AdminDAO(con);
            if(cod.equals("")){
                JOptionPane.showMessageDialog(null, "Você não digitou nenhuma senha.", "JKLR Sistemas", JOptionPane.WARNING_MESSAGE);
            }else{
                    if(sql.VerificarSenha(cod)==false){
                        JOptionPane.showMessageDialog(null, "Senha incorreta, tente novamente.", "JKLR Sistemas", JOptionPane.WARNING_MESSAGE);
                        jtfSenha.setText("");
                    }else{
                        InserirVotacao();
                        jtfSenha.setText("");
                        jb1.requestFocus();
                    }
                
            }
        Conexao.fecharConexao(con);    
    }

