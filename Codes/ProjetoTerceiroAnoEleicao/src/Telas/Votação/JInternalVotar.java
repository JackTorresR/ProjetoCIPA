    private void votarCandidato(){
        Eleicao a = new Eleicao();
        Candidato b = new Candidato();
        Connection con = Conexao.abrirConexao();
        EleicaoDAO adao = new EleicaoDAO(con);
        CandidatoDAO sql = new CandidatoDAO(con);
        int cod = adao.verificarEleicao();
        System.out.println("Codigo da eleição:(Candidato) "+cod);
            if(cod!=0){
                int codCand = Integer.parseInt(jtfCod.getText());
                int codCandidato = sql.SelecionarCodCandidato(codCand);
                System.out.println("Código candidato: "+codCandidato);
                int votos = sql.verificarVotos(codCandidato);
                System.out.println("Quantos votos no candidato: "+votos);
                b.setCod_candidato(codCandidato);
                b.setVotos(votos);
                sql.votar(b);
                
                int validos = sql.verificarValidos(cod);
                System.out.println("Verificar a quantidade de votos válidos(JIVotar):"+validos);
                
                a.setNum_eleicao(cod);
                a.setValidos(validos);
                
                String r = sql.addValido(a);
                System.out.println("Adicionou válidos?(JIVotar):"+r);
                som();
                limparVotacao();
                Conexao.fecharConexao(con);
            }else{
                this.dispose();
                JOptionPane.showMessageDialog(null, "Nenhuma eleição encontrada, encerre a atual e tente novamente.","JKLR Sistemas",JOptionPane.PLAIN_MESSAGE);
            }
    }

